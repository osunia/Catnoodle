    const flipH = (grid) => grid.map(row => [...row].reverse());

    // ë„ìš°ë¯¸: 180ë„ íšŒì „ (Rotate 180 = Flip V + Flip H)
    const rot180 = (grid) => flipH(flipV(grid));

