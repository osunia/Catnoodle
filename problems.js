function generateVariations() {
    let expandedDB = [];
    const clone = (grid) => grid.map(row => [...row]);
    const flipV = (grid) => clone(grid).reverse();
    const flipH = (grid) => grid.map(row => [...row].reverse());
    const rot180 = (grid) => flipH(flipV(grid));

    BASE_SOLUTIONS.forEach(base => {
        expandedDB.push(base);
        expandedDB.push(flipV(base));
        expandedDB.push(flipH(base));
        expandedDB.push(rot180(base));
    });
    return expandedDB;
}

