    // ë„ìš°ë¯¸: 2ì°¨ì› ë°°ì—´ ê¹Šì€ ë³µì‚¬
    const clone = (grid) => grid.map(row => [...row]);

    // ë„ìš°ë¯¸: ìƒí•˜ ë°˜ì „ (Flip Vertical)
    const flipV = (grid) => clone(grid).reverse();

